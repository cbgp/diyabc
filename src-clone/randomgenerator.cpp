#define MODULO 16777216

double sqr(double x) {return x*x;}
 
struct MwcGen
{	unsigned long int mult,mwcx,carry;
//    int modulo = 16777216;

	void randinit(unsigned int indice, unsigned int indice2) {
		this->mult = multipli[indice % 10000];
        srand(indice2);
        this->mwcx = rand() % MODULO;
		srand(indice2*indice);
        this->carry= rand() % MODULO;
        //std::cout << "mwcx = " << this->mwcx << "   carry = " << this->carry << "\n";
	}

        double random() {
		unsigned long int y ;
		double r;
		y = (this->mult)*(this->mwcx) + (this->carry);
		this->mwcx = y % MODULO;
		this->carry = y / MODULO;
		r = (double) this->mwcx /(double)MODULO;
		return r;
	}

	int rand0(int max) {
		double r;
		r = floor(max * this->random());
		return (int) r;
	}

	int rand1(int max) {
		double r;
		r = floor(max * this->random()) + 1.0;
		return (int) r;
	}

	int poisson (double lambda) {
		if (lambda<=0) {return 0;}
		int i=0;
		double p=0.0;
		double ra;
		while (p<lambda) {
			i++;
			do {ra = this->random();} while (ra==0.0);
			p -= log(ra);
		}
		return (i-1);
	}

	double gunif(double min,double max) {
		double u;
		u = min + (max-min)*this->random();
		return u;
	}

	double glogunif(double min,double max) {
		double u,logmin,logmax;
		logmin=log(min);logmax=log(max);
		u = logmin + (logmax-logmin)*this->random();
                //cout <<logmin<<"   "<<logmax<<"   "<<u<<"   "<<exp(u)<<"\n";
		return exp(u);
	}

	double gnormal() {
		const double TWOPI = 6.283185307; //8.0*atan(1.0);
		double u1,u2,c,d;
		do {u1 = this->random();} while (u1<0.0000001);
		u2 = this->random();
		c = sqrt(-2.0*log(u1));
		d = TWOPI*u2;
		if (this->random()<0.5) return c*cos(d);
		else 					return c*sin(d);
	}

	double gnorm(double mean,double sd) {return mean + sd*this->gnormal();}

	double glognorm(double mean,double sd) {
		double u,logmean,logsd;
		logmean=log(mean);logsd=log(sd);
		u = gnorm(logmean,logsd);
		return exp(u);
	}

	double ggamma (double shape, double rate) {
		const double log4=1.3862943611198906188344642429164,sg_magic=2.5040773967762740733732583523869,e=2.7182818284590452353602874713527;
		double alpha,beta,ainv,bbb,ccc,u1,u2,v,x,z,r,res;
		bool fin=false;;
		alpha=shape;
		beta=1.0/rate;
		if (alpha>1.0) {
			ainv = sqrt(2.0*alpha-1.0);
			bbb = alpha - log4;
			ccc = alpha+ainv;
			while (not fin) {
				do {u1 = this->random();} while ((u1<0.0000001)or(u1>0.9999999));
				u2 = 1.0 - this->random();
				v = log(u1/(1.0-u1))/ainv;
				x = alpha*exp(v);
				z = u1*u1*u2;
				r = bbb + ccc*v - x;
				fin = ((r + sg_magic -4.5*z >= 0.0)or(r >= log(z)));
			}
			res = x*beta;
		}
		else {
			if (alpha==1.0) {
				do {u1 = this->random();} while (u1<0.0000001);
				res = -log(u1)*beta;
			}
			if (alpha<1.0) {
				while (not fin) {
					u2 = this->random();
					bbb = (e+alpha)/e;
					v = bbb*u2;
					if (v<=1.0) x = exp((1.0/alpha)*log(v));
					else        x = -log((bbb-v)/alpha);
					u1 = this->random();
					if (v>1.0)  fin = (u1 <= exp((alpha-1.0)*log(x)));
					else         fin = (u1 <= exp(x));
				}
				res = x*beta;
			}
		}
		return res;
	}

	double ggamma2(double mean, double sd) {
		double shape,rate;
		if (sd<=0.0) return -1.0;
		shape = sqr(mean/sd);
		rate = mean/sqr(sd);
		return this->ggamma(shape,rate);
	}

	double ggamma3(double mean, double shape) {
		double sd;
		if (shape==0.0) return 1.0;
		sd = mean/sqrt(shape);
		return this->ggamma2(mean,sd);
	}
};

